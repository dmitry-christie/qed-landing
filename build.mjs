/* QED build step (run by Netlify) — turns deploy env vars into a static config.

   Env vars (set in Netlify → Site settings → Environment variables):
     BRAND                      QED | TDT    (QED = landing.quizeatdrink.com/EN, TDT = landing.tardeodetrivia.com/ES)
     DEV_NOTICE                 true | false (default true — shows the "in development" banner)
     DEFAULT_LANGUAGE           EN | ES      (fallback when BRAND is not set; default EN)
     RUDDERSTACK_WRITE_KEY      live-site write key (used when Netlify's own CONTEXT === 'production')
     RUDDERSTACK_WRITE_KEY_QA   QA write key (used for deploy previews / branch deploys)
     RUDDERSTACK_DATA_PLANE_URL data plane URL from the RudderStack source's Setup tab
     RUDDERSTACK_CDN_URL        the exact SDK <script src> URL from that same Setup tab —
                                deliberately NOT hardcoded here; RudderStack's CDN path is
                                workspace/version-specific and copying a guessed URL risks
                                silently loading nothing on a live site.

   Output:
     - config.js   regenerated with the resolved values.
     - On branded builds, the <!-- build:seo --> marker in each page's <head> is
       replaced with canonical / hreflang / og:url / og:image tags for that domain.
     - When the three RUDDERSTACK_* vars above are all set, the <!-- build:analytics -->
       marker is replaced with an inline snippet carrying those values (baked at build
       time, not read from config.js, so it works regardless of script load order) —
       shared/consent.js reads window.__qedRudder and only loads the CDN script once
       consent is granted.

   Every page lives at its natural folder path (/corporate/, /celebrations/, …) and
   identifies itself with a baked-in window.QED_SITE, so there is no page-copying step. */

import { readFileSync, writeFileSync, unlinkSync, readdirSync } from "node:fs";

const VALID_BRANDS = ["QED", "TDT"];
const brand = VALID_BRANDS.includes((process.env.BRAND || "").toUpperCase())
  ? process.env.BRAND.toUpperCase()
  : null;

// BRAND drives language when set; DEFAULT_LANGUAGE is the local/preview fallback.
const lang = brand === "TDT" ? "ES"
  : brand === "QED" ? "EN"
  : (process.env.DEFAULT_LANGUAGE || "EN").toUpperCase() === "ES" ? "ES" : "EN";

// Explicit DEV_NOTICE wins; otherwise show the banner only on unbranded local/preview
// builds — a branded (QED/TDT) production deploy ships without the "in development" notice.
const devNotice = process.env.DEV_NOTICE != null
  ? String(process.env.DEV_NOTICE).toLowerCase() !== "false"
  : !brand;

const config = { defaultLanguage: lang, devNotice, ...(brand && { brand }) };
writeFileSync(
  "config.js",
  `/* generated by build.mjs from Netlify env vars. Edit env vars, not this file. */\n` +
    `window.QED_CONFIG = ${JSON.stringify(config)};\n`
);

console.log("[qed build] config:", config);

/* ---- per-brand SEO head injection (branded deploys only), + RudderStack analytics
   injection (any deploy with the right env vars, independent of BRAND) ----
   The two brands serve the same pages on different domains (EN on QED, ES on TDT),
   so canonical + hreflang must be stamped at build time. Local/preview builds keep
   the inert <!-- build:seo --> comment. */
// "landing." prefix matches where this site is actually deployed (see Netlify custom
// domains). The bare apex domains are a separate, unrelated site — pointing canonical/
// hreflang tags there would misdirect search engines off this site entirely.
const DOMAINS = { QED: "landing.quizeatdrink.com", TDT: "landing.tardeodetrivia.com" };
const PAGES = ["", "corporate/", "celebrations/", "venues/", "partners/", "privacy/", "terms/"];
const SEO_MARK = "<!-- build:seo -->";
const ANALYTICS_MARK = "<!-- build:analytics -->";
const ANALYTICS_BODY_MARK = "<!-- build:analytics-body -->";

// Per-brand favicon / apple-touch-icon / og:image — see CLAUDE.md "Social preview images".
// og-image-tdt.png is regenerated by shared/make-og-image-tdt.py, not hand-edited.
const BRAND_ASSETS = {
  QED: { icon: "qed-logo.png", ogImage: "og-image.png" },
  TDT: { icon: "tardeo-logo.png", ogImage: "og-image-tdt.png" },
};

// <title>/<meta> tags carry both the baked English and a data-i18n(-content) key. Crawlers
// (WhatsApp, Facebook, Slack …) fetch the raw HTML and never run shared/i18n.js, so on a
// TDT build these must be swapped to Spanish here — the key's client-side ES value would
// otherwise never reach a link-preview bot. Pulled straight from the i18n-*.js files so
// there is exactly one place each Spanish string is written.
const I18N_FILE = {
  "": "hub", "corporate/": "corporate", "celebrations/": "celebrations",
  "venues/": "venues", "partners/": "partners", "privacy/": "privacy", "terms/": "terms",
};
function loadEsDict(page) {
  return readFileSync("shared/i18n-common.js", "utf8") + readFileSync(`shared/i18n-${I18N_FILE[page]}.js`, "utf8");
}
function esValue(dictText, key) {
  const m = dictText.match(new RegExp(`"${key.replace(/\./g, "\\.")}"\\s*:\\s*"((?:[^"\\\\]|\\\\.)*)"`));
  return m ? m[1].replace(/\\(.)/g, "$1") : null;
}
function localizeHead(html, page) {
  const dict = loadEsDict(page);
  html = html.replace(/<title data-i18n="([^"]+)">[^<]*<\/title>/, (full, key) => {
    const es = esValue(dict, key);
    return es ? `<title data-i18n="${key}">${es}</title>` : full;
  });
  html = html.replace(
    /(<meta[^>]*\bdata-i18n-content="([^"]+)"[^>]*\bcontent=")[^"]*("[^>]*>)/g,
    (full, pre, key, post) => {
      const es = esValue(dict, key);
      return es ? `${pre}${es}${post}` : full;
    },
  );
  return html;
}

// Live vs QA write key follows Netlify's own deploy CONTEXT (production | deploy-preview |
// branch-deploy) — set by Netlify itself, no extra config needed to distinguish them.
const rudderWriteKey = process.env.CONTEXT === "production"
  ? process.env.RUDDERSTACK_WRITE_KEY
  : process.env.RUDDERSTACK_WRITE_KEY_QA;
const rudderDataPlaneUrl = process.env.RUDDERSTACK_DATA_PLANE_URL;
const rudderCdnUrl = process.env.RUDDERSTACK_CDN_URL;
const rudderReady = Boolean(rudderWriteKey && rudderDataPlaneUrl && rudderCdnUrl);

/* ---- robots.txt + sitemap.xml (runs on every build, branded or not) ----
   Both are root-level static files, so — like config.js — they're fully regenerated here
   rather than marker-patched. A branded build gets a real sitemap and full crawl access; an
   unbranded build (local/preview, *.netlify.app) blocks crawling outright, same spirit as
   consent.js never firing analytics off the two production domains — a preview should never
   get indexed under the wrong URL. */
if (brand) {
  const domain = DOMAINS[brand];
  writeFileSync("robots.txt", `User-agent: *\nAllow: /\n\nSitemap: https://${domain}/sitemap.xml\n`);
  const urls = PAGES.map((page) => `  <url><loc>https://${domain}/${page}</loc></url>`).join("\n");
  writeFileSync("sitemap.xml", `<?xml version="1.0" encoding="UTF-8"?>\n<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n${urls}\n</urlset>\n`);
  console.log(`[qed build] wrote robots.txt + sitemap.xml for ${domain}`);
} else {
  writeFileSync("robots.txt", "User-agent: *\nDisallow: /\n");
  writeFileSync("sitemap.xml", '<?xml version="1.0" encoding="UTF-8"?>\n<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n</urlset>\n');
}

/* ---- shared "About us" injection (runs on every build, branded or not) ----
   The About section is identical on every page, so it lives in one partial
   (shared/about.partial.html) and is stamped into each page's <!-- build:about -->
   marker here. Edit the partial once and every page updates. Pages without the
   marker (privacy, terms) are left untouched. Copy is still translated at runtime
   via the about.* keys in shared/i18n-common.js. */
const ABOUT_MARK = "<!-- build:about -->";
const aboutPartial = readFileSync("shared/about.partial.html", "utf8")
  .replace(/^\s*<!--[\s\S]*?-->\s*/, "") // drop the partial's dev-note comment; don't ship it
  .trimEnd();
let aboutInjected = 0;
for (const page of PAGES) {
  const file = `${page}index.html`;
  const html = readFileSync(file, "utf8");
  if (!html.includes(ABOUT_MARK)) continue;
  writeFileSync(file, html.replace(ABOUT_MARK, aboutPartial));
  aboutInjected++;
}
console.log(`[qed build] injected About section into ${aboutInjected} page(s)`);

if (brand || rudderReady) {
  let seoInjected = 0;
  let analyticsInjected = 0;
  for (const page of PAGES) {
    const file = `${page}index.html`;
    let html = readFileSync(file, "utf8");
    let changed = false;

    if (brand) {
      const domain = DOMAINS[brand];
      const assets = BRAND_ASSETS[brand];
      if (html.includes(SEO_MARK)) {
        const tags = [
          `<link rel="canonical" href="https://${domain}/${page}">`,
          `<link rel="alternate" hreflang="en" href="https://${DOMAINS.QED}/${page}">`,
          `<link rel="alternate" hreflang="es" href="https://${DOMAINS.TDT}/${page}">`,
          `<link rel="alternate" hreflang="x-default" href="https://${DOMAINS.QED}/${page}">`,
          `<meta property="og:url" content="https://${domain}/${page}">`,
          `<meta property="og:image" content="https://${domain}/shared/${assets.ogImage}">`,
          `<meta property="og:image:width" content="1200">`,
          `<meta property="og:image:height" content="630">`,
          `<meta property="og:locale" content="${lang === "ES" ? "es_ES" : "en_US"}">`,
        ].join("\n");
        html = html.replace(SEO_MARK, tags);
        if (lang === "ES") html = html.replace('<html lang="en">', '<html lang="es">');
        changed = true;
        seoInjected++;
      } else {
        console.warn(`[qed build] ${file}: no ${SEO_MARK} marker, skipped`);
      }

      html = html
        .replace('<link rel="icon" href="/shared/qed-logo.png">', `<link rel="icon" href="/shared/${assets.icon}">`)
        .replace('<link rel="apple-touch-icon" href="/shared/qed-logo.png">', `<link rel="apple-touch-icon" href="/shared/${assets.icon}">`);
      if (lang === "ES") html = localizeHead(html, page);
      changed = true;
    }

    if (rudderReady && html.includes(ANALYTICS_MARK)) {
      const cfg = JSON.stringify({
        writeKey: rudderWriteKey,
        dataPlaneUrl: rudderDataPlaneUrl,
        cdnUrl: rudderCdnUrl,
      });
      html = html.replace(ANALYTICS_MARK, `<script>window.__qedRudder=${cfg};</script>`);
      if (html.includes(ANALYTICS_BODY_MARK)) html = html.replace(ANALYTICS_BODY_MARK, "");
      changed = true;
      analyticsInjected++;
    }

    if (changed) writeFileSync(file, html);
  }
  if (brand) console.log(`[qed build] injected SEO head tags for ${DOMAINS[brand]} into ${seoInjected}/${PAGES.length} pages`);
  if (rudderReady) console.log(`[qed build] injected RudderStack config into ${analyticsInjected}/${PAGES.length} pages`);
} else {
  console.log("[qed build] no BRAND and no RudderStack env vars set — build:seo / build:analytics markers left inert");
}

/* ---- strip internal-only files from the ephemeral publish tree (runs on every build) ----
   publish = "." serves the whole repo root as static files, so anything committed here that
   isn't meant for visitors (planning docs, tooling config, this script) is reachable at its
   literal URL unless removed before Netlify uploads the build. This never touches netlify/ —
   Netlify Functions are bundled from that source directory as a separate build phase, so it
   must still exist on disk after this script runs; netlify.toml blocks it at the HTTP layer
   instead (see the /netlify/* redirect). None of this is committed — it only edits the
   ephemeral build tree, restored by `git checkout -- .` after a local `node build.mjs` run. */
const NAMED_INTERNAL_FILES = [
  "package.json",
  "package-lock.json",
  "tsconfig.json",
  "deno.lock",
];
let stripped = 0;
for (const file of readdirSync(".")) {
  if (file.endsWith(".md") || NAMED_INTERNAL_FILES.includes(file)) {
    unlinkSync(file);
    stripped++;
  }
}
console.log(`[qed build] stripped ${stripped} internal file(s) from the publish tree`);
unlinkSync("build.mjs");
